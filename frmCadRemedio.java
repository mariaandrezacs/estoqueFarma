    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        DBRemedio db = new DBRemedio();
        
        if(lblId.getText() == "0"){
            
            Remedio rem = new Remedio(txtLaboratorio.getText(), txtNome.getText(), Double.parseDouble(txtPreco.getText()));
            db.inserir(rem);
            
        } else {
            Remedio rem = new Remedio(Integer.valueOf(lblId.getText()),txtLaboratorio.getText(), txtNome.getText(), Double.parseDouble(txtPreco.getText()));
            db.alterar(rem);
        }

        JOptionPane.showMessageDialog(this, "Remedio " + txtNome.getText() + " inserido com sucesso!!!");
        limpaCampos();
        
    }//GEN-LAST:event_btnSalvarActionPerformed

